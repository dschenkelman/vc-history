// Assign nodes to their respective lines
subwayLines.forEach((line) => {
  line.nodes = subwayNodes
    .filter((node) => node.lineId === line.id)
    .sort((a, b) => new Date(a.date).getTime() - new Date(b.date).getTime())
    .map((node) => node.id)
})

const parseDate = (dateString: string): Date => {
  const [year, month, day] = dateString.split("-").map(Number)
  return new Date(year, month - 1, day)
}

const getTimelineRange = (): [Date, Date] => {
  let minDate = new Date()
  let maxDate = new Date(2020, 11, 31) // End when the book ends

  subwayNodes.forEach((node) => {
    const nodeDate = parseDate(node.date)
    if (nodeDate < minDate) minDate = nodeDate
    if (nodeDate > maxDate && nodeDate.getFullYear() <= 2020) maxDate = nodeDate
  })

  minDate.setFullYear(minDate.getFullYear() - 2)
  return [minDate, maxDate]
}
