// Comprehensive VC history data structured for subway-style visualization
// Based on "The Power Law" by Sebastian Mallaby

export interface SubwayNode {
  id: string
  date: string // YYYY-MM-DD
  title: string
  description: string
  type: "firm_founding" | "investment" | "ipo" | "acquisition" | "person_move" | "milestone"
  x?: number // Will be calculated based on date
  y?: number // Will be calculated based on line
  connections: string[] // IDs of connected nodes
  lineId: string
  importance: 1 | 2 | 3 // 1=minor, 2=major, 3=legendary
  link?: string // Optional link for more details
}

export interface SubwayLine {
  id: string
  name: string
  color: string
  startDate: string
  endDate?: string // undefined means still active
  parentLineId?: string // for lines that spawn from others
  nodes: string[] // node IDs in chronological order
}

